package com.huaxia.john;

public class Math1 {
	public int add(int x, int y) {
		return x + y;
	}
	public int sub(int x, int y) {
		return x - y;
	}
	public int mul(int x, int y) {
		return x * y;
	}
	int div(int x, int y) {
		return x/y;
	}
	
	double circlearea(double r) {
		return r*r*Math.PI;
	}
	
	double rectangleArea(double width, double height) {
		return width*height;
	}
}
